package org.example;

public class Demonstrator {
    private String myId;
    private Integer myInteger;
    private long myLongPrimitive;
    private Long myLongObject;


    public void setMyId(String in) {
        this.myId = in;
    }
    public String getMyId() {
        return this.myId;
    }

    public void setMyInteger(Integer in) {
        this.myInteger = in;
    }
    public Integer getMyInteger() {
        return this.myInteger;
    }

    public void setMyLongPrimitive(long in) {
        this.myLongPrimitive = in;
    }
    public long getMyLongPrimitive() {
        return this.myLongPrimitive;
    }

    public void setMyLongObject(Long in) {
        this.myLongObject = in;
    }
    public Long getMyLongObject() {
        return this.myLongObject;
    }
}
